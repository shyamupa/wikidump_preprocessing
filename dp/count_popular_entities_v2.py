    _, t2id, _ = load_id2title(args["id2title"])
    p = EntityCounter(wikipath=args["wikitext"],
                      linksout=args["linksout"],
                      contsout=args["contsout"],
                      redirect_map=redirect_map, t2id=t2id,
                      debug=args["debug"], limit=0)
